def фрукт_в_словаре(словарь):
  """
  Проверяет наличие элемента с ключом "фрукт = яблоко" в словаре.
  Если ключ отсутствует, добавляет его.

  Args:
    словарь: Словарь, в котором происходит проверка.

  Returns:
    tuple: (Первоначальный словарь, Измененный словарь).
  """
  
  фрукт = "фрукт = яблоко"

  try:
    словарь[фрукт]  # Проверка ключа
  except KeyError:
    print(f"Ключ '{фрукт}' не найден в словаре.")
    словарь[фрукт] = "яблоко"  # Добавление ключа и значения
  
  return словарь, словарь.copy()  # Возвращаем оба словаря

# Пример использования
фруктовый_словарь = {"овощ": "морковь", "ягода": "клубника"}
исходный_словарь, измененный_словарь = фрукт_в_словаре(фруктовый_словарь.copy())

print("Исходный словарь:", исходный_словарь)
print("Измененный словарь:", измененный_словарь)
